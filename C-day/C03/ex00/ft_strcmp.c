int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	f;

	f = 0;
	while (s1[f] == s2[f] && s1[f] != '\0' && s2[f] != '\0')
	{
		f++;
	}
	return (s1[f] - s2[f]);
}
