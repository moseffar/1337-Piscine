int	ft_sqrt(int nb)
{
	long	i;
	long	str;

	str = nb;
	if (str <= 0)
		return (0);
	if (str == 1)
		return (1);
	i = 2;
	if (str >= 2)
	{
		while (i * i <= str)
		{
			if (i * i == str)
				return (i);
			i++;
		}
	}
	return (0);
}
