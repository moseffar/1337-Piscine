#include <unistd.h>

int	main(int ac, char **av)
{
	int	a;
	int	b;

	a = 1;
	while (ac > 1 && av[a] != '\0')
	{
		a++;
	}
	a--;
	while (a > 0)
	{
		b = 0;
		while (av[a][b] != '\0')
		{
			write(1, &av[a][b], 1);
			b++;
		}
		write(1, "\n", 1);
		a--;
	}
}
